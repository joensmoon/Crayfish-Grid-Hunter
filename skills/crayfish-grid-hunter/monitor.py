@dataclass
class MonitorThresholds:
    """Configurable thresholds for all alert conditions."""

    # --- Grid Performance ---
    pnl_loss_critical_pct: float = -5.0    # CRITICAL: PnL below -5%
    pnl_loss_high_pct: float = -3.0        # HIGH: PnL below -3%
    fill_rate_low_pct: float = 20.0        # MEDIUM: Fill rate below 20%
    fill_rate_stalled_pct: float = 5.0     # HIGH: Fill rate below 5% (grid stalled)

    # --- Market Condition ---
    boundary_proximity_critical_pct: float = 3.0   # CRITICAL: Price within 3% of boundary
    boundary_proximity_high_pct: float = 8.0        # HIGH: Price within 8% of boundary
    volume_spike_multiplier: float = 2.5            # CRITICAL: Volume > 2.5× 24h average
    trend_drift_pct: float = 2.0                    # MEDIUM: Price drifted >2% from entry

    # --- Risk Management ---
    stop_loss_proximity_critical_pct: float = 2.0  # CRITICAL: Within 2% of stop-loss
    stop_loss_proximity_high_pct: float = 5.0      # HIGH: Within 5% of stop-loss
    max_drawdown_critical_pct: float = 8.0         # CRITICAL: Drawdown > 8%
    max_drawdown_high_pct: float = 5.0             # HIGH: Drawdown > 5%

    # --- API Health ---
    api_latency_medium_ms: float = 1000.0   # MEDIUM: Avg latency > 1s
    api_latency_high_ms: float = 3000.0     # HIGH: Avg latency > 3s
    api_error_rate_medium_pct: float = 5.0  # MEDIUM: Error rate > 5%
    api_error_rate_high_pct: float = 20.0   # HIGH: Error rate > 20%


