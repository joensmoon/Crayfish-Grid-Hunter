def create_monitor(
    pnl_loss_critical_pct: float = -5.0,
    pnl_loss_high_pct: float = -3.0,
    boundary_proximity_critical_pct: float = 3.0,
    stop_loss_proximity_critical_pct: float = 2.0,
    volume_spike_multiplier: float = 2.5,
    api_latency_high_ms: float = 3000.0,
) -> GridPerformanceMonitor:
    """Create a pre-configured monitor with commonly adjusted thresholds."""
    thresholds = MonitorThresholds(
        pnl_loss_critical_pct=pnl_loss_critical_pct,
        pnl_loss_high_pct=pnl_loss_high_pct,
        boundary_proximity_critical_pct=boundary_proximity_critical_pct,
        stop_loss_proximity_critical_pct=stop_loss_proximity_critical_pct,
        volume_spike_multiplier=volume_spike_multiplier,
        api_latency_high_ms=api_latency_high_ms,
    )
    return GridPerformanceMonitor(thresholds=thresholds)
